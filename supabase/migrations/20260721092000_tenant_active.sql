-- =============================================================================
-- Выключение сайта клиента
--
-- Сервис продаётся по подписке, значит нужен рычаг «перестал платить — сайт
-- погас». До сих пор единственным способом было удалить тенанта, то есть
-- вместе с сайтом стереть его меню и новости. Для человека, который может
-- вернуться через месяц, это несоразмерно.
--
-- is_active = false означает: публичный сайт не открывается, данные целы,
-- владелец по-прежнему заходит в панель и всё видит. Включили обратно —
-- сайт вернулся ровно таким, каким был.
--
-- Проверка висит на политике публичного чтения tenants, а не на коде: сайт
-- не резолвится по слагу, и дальше resolveTenant() всё равно упрётся в 404.
-- Политику владельца это не трогает — иначе выключенный клиент потерял бы
-- доступ к собственным данным и не смог бы их забрать.
-- =============================================================================

alter table public.tenants
  add column if not exists is_active boolean not null default true;

comment on column public.tenants.is_active is
  'false — публичный сайт выключен (например, за неоплату). Данные и доступ в панель сохраняются.';

-- Колонку надо отдать наружу: без неё select с перечислением колонок упрётся
-- в permission denied, а select('*') по tenants запрещён грантами.
grant select (id, slug, name, custom_domain, is_active) on public.tenants to anon, authenticated;

drop policy if exists "tenants: публичное чтение" on public.tenants;
create policy "tenants: публичное чтение"
  on public.tenants for select
  to anon
  using (is_active = true);
