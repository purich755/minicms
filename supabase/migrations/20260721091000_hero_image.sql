-- =============================================================================
-- Картинка в шапку публичного сайта
--
-- Главная страница держалась на одном заголовке. Для кофейни или барбершопа
-- это слабо: у них товар визуальный, и первое, что должен увидеть гость, —
-- как у них выглядит, а не как называется.
--
-- Отдельная колонка, а не logo_url: логотип и фотография заведения нужны
-- одновременно и в разных местах. Логотип уходит в шапку и в значок вкладки,
-- фотография — на первый экран.
--
-- Гранты не нужны: на site_settings они выданы на всю таблицу, а не поимённо
-- по колонкам, поэтому новая колонка доступна сразу.
-- =============================================================================

alter table public.site_settings
  add column if not exists hero_image_url text;

comment on column public.site_settings.hero_image_url is
  'Фотография заведения на первый экран сайта. null — показываем только текст.';
